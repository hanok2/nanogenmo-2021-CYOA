	function presentVerb($word)
	{
		global $verbList;
		if (isset($verbList[$word])) { return $verbList[$word]; }
		if (in_array($word,$verbList)) { return $word; }
		return "";
	}
	
	function cleanQuotes($string)
	{
		$string = iconv('UTF-8', 'ASCII//TRANSLIT', $string);
